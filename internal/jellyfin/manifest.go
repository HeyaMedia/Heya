package jellyfin

// The coverage manifest: every operation in the vendored Jellyfin 10.11.11
// OpenAPI spec (spec/jellyfin-openapi-10.11.11.json.gz), triaged. This is
// how "reimplement the entire Jellyfin API" stays honest — manifest_test.go
// fails the build when a spec operation is missing here, when an entry
// claims implemented/stubbed without a matching registered route, or when a
// registered route doesn't exist in the spec (pattern typo).
//
// Statuses:
//
//	opImplemented — registered, real behavior backed by Heya services.
//	opStubbed     — registered, returns the same "feature absent/disabled"
//	                shape a stock Jellyfin with that feature off returns.
//	opPlanned     — not yet registered; the phase 1–3 work queue.
//	opOutOfScope  — no Heya equivalent (LiveTV tuners, plugin market,
//	                server config dashboard...). Phase 4 gives the probed
//	                ones graceful stubs; the rest 404 like disabled
//	                features do upstream.
type opStatus int

const (
	opPlanned opStatus = iota
	opImplemented
	opStubbed
	opOutOfScope
)

type manifestEntry struct {
	Status opStatus
	Tag    string
}

