type recordingObserver struct{ outputs []Output }

func (r *recordingObserver) SuppressGeneratedWrite(output Output) error {
	r.outputs = append(r.outputs, output)
	return nil
}

func TestPublishIntentCreatesAndRefreshesGeneratedSidecar(t *testing.T) {
	pool := testutil.SetupDB(t)
	root := t.TempDir()
	createGeneratedTestLibrary(t, pool, root, "publisher")
	dir := filepath.Join(root, "Ado")
	require.NoError(t, os.MkdirAll(dir, 0o755))
	path := filepath.Join(dir, "artist.nfo")
	observer := &recordingObserver{}

	first := []byte("first generated bytes")
	prepared, err := PrepareBytes(path, 0o644, first)
	require.NoError(t, err)
	output, outcome, err := Publish(context.Background(), pool, observer, prepared)
	require.NoError(t, err)
	require.Equal(t, OutcomePublished, outcome)
	require.True(t, output.Written)
	require.Equal(t, first, mustReadFile(t, path))
	firstInfo, err := os.Stat(path)
	require.NoError(t, err)

	second := []byte("second generated bytes with refresh")
	prepared, err = PrepareBytes(path, 0o644, second)
	require.NoError(t, err)
	output, outcome, err = Publish(context.Background(), pool, observer, prepared)
	require.NoError(t, err)
	require.Equal(t, OutcomePublished, outcome)
	require.True(t, output.Written)
	require.Equal(t, second, mustReadFile(t, path))
	secondInfo, err := os.Stat(path)
	require.NoError(t, err)
	require.False(t, os.SameFile(firstInfo, secondInfo), "refresh atomically installs the staged inode")

	publication, exists, err := LoadPublication(context.Background(), pool, output.Path)
	require.NoError(t, err)
	require.True(t, exists)
	require.Nil(t, publication.Pending)
	require.NotNil(t, publication.Published)
	require.True(t, publication.Published.Matches(int64(len(second)), sha256.Sum256(second)))
	require.Len(t, observer.outputs, 2)
}

func TestPublishOnlyAdoptsExactDeterministicUnprovenancedFile(t *testing.T) {
	pool := testutil.SetupDB(t)
	root := t.TempDir()
	createGeneratedTestLibrary(t, pool, root, "user-wins")
	path := filepath.Join(root, "movie.nfo")
	user := []byte("user-owned")
	require.NoError(t, os.WriteFile(path, user, 0o644))
	before, err := os.Stat(path)
	require.NoError(t, err)

	prepared, err := PrepareBytes(path, 0o644, []byte("different generated"))
	require.NoError(t, err)
	output, outcome, err := Publish(context.Background(), pool, nil, prepared)
	require.NoError(t, err)
	require.Equal(t, OutcomeUserOwned, outcome)
	require.False(t, output.Attested)
	require.Equal(t, user, mustReadFile(t, path))

	prepared, err = PrepareBytes(path, 0o644, user)
	require.NoError(t, err)
	output, outcome, err = Publish(context.Background(), pool, nil, prepared)
	require.NoError(t, err)
	require.Equal(t, OutcomeAttested, outcome)
	require.True(t, output.Attested)
	require.False(t, output.Written)
	require.Equal(t, user, mustReadFile(t, path))
	after, err := os.Stat(path)
	require.NoError(t, err)
	require.True(t, os.SameFile(before, after), "bootstrap attestation must not rewrite the existing file")
	require.Equal(t, before.ModTime(), after.ModTime(), "bootstrap attestation must preserve mtime")

	canonical, err := CanonicalPath(path)
	require.NoError(t, err)
	publication, exists, err := LoadPublication(context.Background(), pool, canonical)
	require.NoError(t, err)
	require.True(t, exists)
	require.NotNil(t, publication.Published)
	require.True(t, publication.Published.Matches(int64(len(user)), sha256.Sum256(user)))
}

func TestPublishRecoveryRestoresRacedUserPredecessor(t *testing.T) {
	pool := testutil.SetupDB(t)
	root := t.TempDir()
	library := createGeneratedTestLibrary(t, pool, root, "restore-user")
	path, err := CanonicalPath(filepath.Join(root, "album.nfo"))
	require.NoError(t, err)
	oldGenerated := []byte("old-generated")
	desired := []byte("new-generated")
	user := []byte("user-content!")
	require.Len(t, user, len(oldGenerated))
	require.NoError(t, os.WriteFile(path, desired, 0o644))
	destInfo, err := os.Stat(path)
	require.NoError(t, err)
	previous := filepath.Join(filepath.Dir(path), ".heya-generated-"+uuid.NewString()+".previous")
	staged := filepath.Join(filepath.Dir(path), ".heya-atomic-album.nfo.crashed.tmp")
	require.NoError(t, os.WriteFile(previous, user, 0o644))
	oldDigest, desiredDigest := sha256.Sum256(oldGenerated), sha256.Sum256(desired)
	intentID := uuid.New()
	_, err = pool.Exec(context.Background(), `
		INSERT INTO generated_sidecar_publications (
			path, published_size, published_mtime, published_sha256, published_at,
			pending_intent_id, pending_size, pending_sha256,
			pending_staged_path, pending_previous_path, pending_lease_expires_at
		) VALUES ($1,$2,$3,$4,now(),$5,$6,$7,$8,$9,now()+interval '10 minutes')
	`, path, len(oldGenerated), destInfo.ModTime(), oldDigest[:], intentID,
		len(desired), desiredDigest[:], staged, previous)
	require.NoError(t, err)
	_, err = pool.Exec(context.Background(), `INSERT INTO library_generated_sidecars (library_id,path) VALUES ($1,$2)`, library.ID, path)
	require.NoError(t, err)

	prepared, err := PrepareBytes(path, 0o644, desired)
	require.NoError(t, err)
	_, outcome, err := Publish(context.Background(), pool, nil, prepared)
	require.NoError(t, err)
	require.Equal(t, OutcomeUserOwned, outcome)
	require.Equal(t, user, mustReadFile(t, path), "the displaced user predecessor must be atomically restored")
	_, exists, err := LoadPublication(context.Background(), pool, path)
	require.NoError(t, err)
	require.False(t, exists)
}

