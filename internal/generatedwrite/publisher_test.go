func TestReconcileContinuesPastBrokenPublication(t *testing.T) {
	pool := testutil.SetupDB(t)
	ctx := context.Background()
	root := t.TempDir()
	library := createGeneratedTestLibrary(t, pool, root, "reconcile-bounded-failure")

	validPath := filepath.Join(root, "valid.nfo")
	prepared, err := PrepareBytes(validPath, 0o644, []byte("valid generated bytes"))
	require.NoError(t, err)
	output, _, err := Publish(ctx, pool, nil, prepared)
	require.NoError(t, err)
	validPath = output.Path

	brokenPath := filepath.Join(root, strings.Repeat("x", 300), "broken.nfo")
	brokenDigest := sha256.Sum256([]byte("unreachable"))
	_, err = pool.Exec(ctx, `
		INSERT INTO generated_sidecar_publications (
			path, published_size, published_mtime, published_sha256,
			published_at, verified_at
		) VALUES ($1, $2, now(), $3, now(), '1900-01-01'::timestamptz)
	`, brokenPath, len("unreachable"), brokenDigest[:])
	require.NoError(t, err)
	_, err = pool.Exec(ctx, `INSERT INTO library_generated_sidecars (library_id,path) VALUES ($1,$2)`, library.ID, brokenPath)
	require.NoError(t, err)
	_, err = pool.Exec(ctx, `UPDATE generated_sidecar_publications SET verified_at = '1901-01-01'::timestamptz WHERE path = $1`, validPath)
	require.NoError(t, err)
	t.Cleanup(func() {
		_, _ = pool.Exec(context.Background(), `DELETE FROM generated_sidecar_publications WHERE path = ANY($1::text[])`, []string{brokenPath, validPath})
	})

	counts, reconcileErr := Reconcile(ctx, pool, 2)
	require.Error(t, reconcileErr)
	require.Equal(t, 1, counts.Failed)
	require.Equal(t, 1, counts.Examined, "the valid publication after the broken row must still be examined")

	var verifiedAt time.Time
	require.NoError(t, pool.QueryRow(ctx, `SELECT verified_at FROM generated_sidecar_publications WHERE path = $1`, validPath).Scan(&verifiedAt))
	require.True(t, verifiedAt.After(time.Date(2000, 1, 1, 0, 0, 0, 0, time.UTC)), "later publication was starved behind the broken row")
}

func createGeneratedTestLibrary(t *testing.T, pool *pgxpool.Pool, root, suffix string) sqlc.Library {
	t.Helper()
	library, err := sqlc.New(pool).CreateLibrary(context.Background(), sqlc.CreateLibraryParams{
		Name: "generatedwrite-" + suffix + "-" + uuid.NewString(), MediaType: sqlc.MediaTypeMusic,
		Paths: []string{root}, ScanInterval: pgtype.Interval{Microseconds: int64(time.Hour / time.Microsecond), Valid: true},
		CreatedBy: testutil.TestUserID(t, pool), Settings: []byte("{}"),
	})
	require.NoError(t, err)
	t.Cleanup(func() { testutil.CleanupLibrary(t, pool, library.ID) })
	return library
}

func mustReadFile(t *testing.T, path string) []byte {
	t.Helper()
	content, err := os.ReadFile(path)
	require.NoError(t, err)
	return content
}
